    private void btn_cadsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cadsalvarActionPerformed
        if(f_cadmednome.getText().isEmpty() || f_cadmednasc.getText().isEmpty() || f_cadmedcrm.getText().isEmpty() 
           || f_cadmedend.getText().isEmpty() || f_cadmedemail.getText().isEmpty() || f_cadmedtel.getText().isEmpty()){
            
            JOptionPane.showMessageDialog(null, "Preeencha todos os campos.");
            f_cadmednome.requestFocus();
            
                        
        }else{
            if(cont == 1){
            meddto.setNome(f_cadmednome.getText());
            meddto.setNasc(Integer.parseInt(f_cadmednasc.getText()));
            meddto.setCrm(Integer.parseInt(f_cadmedcrm.getText()));
            meddto.setEndereco(f_cadmedend.getText());
            meddto.setEmail(f_cadmedemail.getText());
            meddto.setTelefone(f_cadmedtel.getText());
            meddto.setEspecializacao((String) jEspecializacao.getSelectedItem());

            meddao.Salvar(meddto);

            f_cadmednome.setText("");
            f_cadmednasc.setText("");
            f_cadmedcrm.setText("");
            f_cadmedend.setText("");
            f_cadmedemail.setText("");
            f_cadmedtel.setText("");

            f_cadmednome.setEnabled(false);
            f_cadmednasc.setEnabled(false);
            f_cadmedcrm.setEnabled(false);
            f_cadmedend.setEnabled(false);
            f_cadmedemail.setEnabled(false);
            f_cadmedtel.setEnabled(false);
            jEspecializacao.setEnabled(false);
            btn_cadsalvar.setEnabled(false);
            btn_cadcancel.setEnabled(false);
            preencherTabela("SELECT * FROM cadmedicos ORDER BY nome_med");
            }
            else{
                meddto.setCodigo(Integer.parseInt(f_cadmedcod.getText()));
                meddto.setNome(f_cadmednome.getText());
                meddto.setNasc(Integer.parseInt(f_cadmednasc.getText()));
                meddto.setCrm(Integer.parseInt(f_cadmedcrm.getText()));
                meddto.setEndereco(f_cadmedend.getText());
                meddto.setEmail(f_cadmedemail.getText());
                meddto.setTelefone(f_cadmedtel.getText());
                meddto.setEspecializacao((String) jEspecializacao.getSelectedItem());
            
                meddao.editar(meddto);

                f_cadmednome.setEnabled(false);
                f_cadmednasc.setEnabled(false);
                f_cadmedcrm.setEnabled(false);
                f_cadmedend.setEnabled(false);
                f_cadmedemail.setEnabled(false);
                f_cadmedtel.setEnabled(false);
                jEspecializacao.setEnabled(false);
                btn_cadsalvar.setEnabled(false);
                btn_cadnovo.setEnabled(true);
                btn_cadcancel.setEnabled(false);

                f_cadmednome.setText("");
                f_cadmednasc.setText("");
                f_cadmedcrm.setText("");
                f_cadmedend.setText("");
                f_cadmedemail.setText("");
                f_cadmedtel.setText("");
                f_cadmedcod.setText("");
                
                preencherTabela("SELECT * FROM cadmedicos ORDER BY nome_med");
                }
            }
        
    }//GEN-LAST:event_btn_cadsalvarActionPerformed

