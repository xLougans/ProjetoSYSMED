    public void preencherTabela(String sql){
        
        ArrayList dados = new ArrayList();
        String [] colunas = new String []{"ID", "Nome", "Nascimento", "CRM", "Endereço", "E-mail", "Telefone", "Especialização"};
        conn.conect();
        conn.executaSql(sql);
        
        try {
            conn.rs.first();
            do {                
                dados.add(new Object[]{conn.rs.getInt("cod_med"), conn.rs.getString("nome_med"), conn.rs.getInt("nasc_med"), 
                    conn.rs.getInt("crm_med"), conn.rs.getString("end_med"), conn.rs.getString("email_med"), 
                    conn.rs.getString("tel_med"), conn.rs.getString("espec_med")});
                                    
            } while (conn.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Insira outro nome para preencher a tabela!");
        }
        
        TabelaDTO tabdto = new TabelaDTO(dados, colunas);
                
        table_cadmed.setModel(tabdto);
        table_cadmed.getColumnModel().getColumn(0).setPreferredWidth(23);
        table_cadmed.getColumnModel().getColumn(0).setResizable(false);
        table_cadmed.getColumnModel().getColumn(1).setPreferredWidth(120);
        table_cadmed.getColumnModel().getColumn(1).setResizable(false);
        table_cadmed.getColumnModel().getColumn(2).setPreferredWidth(75);
        table_cadmed.getColumnModel().getColumn(2).setResizable(false);
        table_cadmed.getColumnModel().getColumn(3).setPreferredWidth(75);
        table_cadmed.getColumnModel().getColumn(3).setResizable(false);
        table_cadmed.getColumnModel().getColumn(4).setPreferredWidth(120);
        table_cadmed.getColumnModel().getColumn(4).setResizable(false);
        table_cadmed.getColumnModel().getColumn(5).setPreferredWidth(160);
        table_cadmed.getColumnModel().getColumn(5).setResizable(false);
        table_cadmed.getColumnModel().getColumn(6).setPreferredWidth(90);
        table_cadmed.getColumnModel().getColumn(6).setResizable(false);
        table_cadmed.getColumnModel().getColumn(7).setPreferredWidth(90);
        table_cadmed.getColumnModel().getColumn(7).setResizable(false);
        
        table_cadmed.getTableHeader().setReorderingAllowed(false);
        table_cadmed.setAutoResizeMode(table_cadmed.AUTO_RESIZE_OFF);
        table_cadmed.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
    }
    
